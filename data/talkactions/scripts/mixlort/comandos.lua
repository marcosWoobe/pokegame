function onSay(cid, words, param, channel)

	doPlayerPopupFYI(cid, [[!pokeballs - Checa as brokes de um pokemon, ex: !pokeballs charmander;
	/h ou !h - Telepora o player para outra cidade, ex: !h cerulean;
	!premium - Mostra as vantagens de ser VIP;
	!shinystone - Mostra a lista de shiny stones necessárias para evoluir cada shiny;
	!aura - Altera a Aura de pokemons capturados pela Premier Ball ou com Particle Aura;
	!walk - Se movimenta automaticamente;
	!buyhouse - Compra uma casa sem dono;
	!leave - Abandona sua casa (seus items vão para o depot);
	!online - Mostra os players online;
	!save - Salva seu personagem.;
	!bug - Use caso esteja preso/bugado para se teleportar para sua cidade natal (Cd: 1d);
	!bless - Checa se possui Bless ativo, se não tiver ele comra automaticamente;
	!expbooster	- Checa quanto tempo tem de exp booster;
	!luz - Ilumina sua tela;
	!guardian - Veja quanto tempo você ainda tem de guardian;
	!autoloot on / !autoloot off - Ativar/Desativar AutoLoot;
	!money - Juntar seu dinheiro
	!shinycharm - Checa quanto tempo resta do seu shiny charm.]])
	return true
end
