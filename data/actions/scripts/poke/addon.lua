function onUse(cid, item, fromPosition, item2, toPosition)

    if item.itemid == 12339 then
	    local addonItem = addonsBox[math.random(#addonsBox)]
		doRemoveItem(item.uid, 1)
		doPlayerAddItem(cid, addonItem, 1) 
	    doPlayerSendTextMessage(cid, 27, "Parabéns você tirou um, "..addons[addonItem].name.." addon para o "..addons[addonItem].pokemon..".")
	    return true
	end
    if #getCreatureSummons(cid) < 1 then
        doPlayerSendTextMessage(cid, 22, "Please, let go your pokemon.")
        return false
    end

    local id = addons[item.itemid].id
    local pb = getPlayerSlotItem(cid, 8).uid
    local pk = addons[item.itemid].pokemon
	
	if not id then
	    doPlayerSendTextMessage(cid, MESSAGE_STATUS_CONSOLE_RED, "Sorry, this is not a addon.")
        return false
	end
	
	if not pk then
	    doPlayerSendTextMessage(cid, MESSAGE_STATUS_CONSOLE_RED, "Sorry, this is not addon of pokemon.")
        return false 
	end
 
    if not isMonster(item2.uid) then
	    doPlayerSendTextMessage(cid, MESSAGE_STATUS_CONSOLE_RED, "Sorry, this is for to be used in pokemon.")
	    return false
	end
	
	if getCreatureMaster(item2.uid) ~= cid or doCorrectPokemonName(getCreatureName(item2.uid)) ~= doCorrectPokemonName(pk) then
        doPlayerSendTextMessage(cid, MESSAGE_STATUS_CONSOLE_RED, "Sorry, you can't use this addon on this pokemon.")
        return false 
    end 
	
	if getItemAttribute(pb, "ehditto") == "Ditto" and getItemAttribute(pb, "poke") ~= "Ditto" or getItemAttribute(pb, "ehditto") == "Shiny Ditto" and getItemAttribute(pb, "poke") ~= "Shiny Ditto" then
        doPlayerSendTextMessage(cid, MESSAGE_STATUS_CONSOLE_RED, "Sorry, revert your "..getItemAttribute(pb, "ehditto")..".")
        return false 
	end
	
    for i = 1, 7 do
        if not getItemAttribute(pb, "addon"..i) then
		    doRemoveItem(item.uid, 1)
			doSetItemAttribute(pb, "addon"..i, id)
            doPlayerSendTextMessage(cid, 27, "Congratulations! Now your "..getItemAttribute(pb, "poke").." will use the "..getItemNameById(item.itemid)..".")
			break
		elseif getItemAttribute(pb, "addon"..i) == id then
		    doPlayerSendTextMessage(cid, MESSAGE_STATUS_CONSOLE_RED, "Sorry, this pokemon already has this addon.")
			break
        end
    end

return true
end