function generateDualList(cid)
	local taskList = {}
	local finalList = {}
	
	for i = 1, #pokemonTaskTable do
		if (getPlayerLevel(cid) >= getTaskDifficultyLevel(pokemonTaskTable[i].difficulty)) and (getPlayerLevel(cid) < getTaskDifficultyLevel(pokemonTaskTable[i].difficulty + 1)) then
			table.insert(taskList, pokemonTaskTable[i].pokemon)
		end
	end
	
	if (#taskList < 1) then
		return false
	end
	
	for i = 0, #taskList do
		local mR = math.random(#taskList)
		if not isInArray(finalList, taskList[mR]) and (#finalList < 2) then
			table.insert(finalList, taskList[mR])
		end
	end

	doCreatureSetStorage(cid, FIRST_POKEMON_ON_LIST, getTaskIdByName(finalList[1]))
	doCreatureSetStorage(cid, SECOND_POKEMON_ON_LIST, getTaskIdByName(finalList[2]))
return true
end

function hasDualList(cid)
	if (getCreatureStorage(cid, FIRST_POKEMON_ON_LIST) >= 1) then
		return true
	end
return false
end

function getFirstTaskPokemon(cid)
	local firstStorage = getCreatureStorage(cid, FIRST_POKEMON_ON_LIST)
	return pokemonTaskTable[firstStorage].pokemon
end

function getSecondTaskPokemon(cid)
	local secondStorage = getCreatureStorage(cid, SECOND_POKEMON_ON_LIST)
	return pokemonTaskTable[secondStorage].pokemon
end

function getFirstTaskPokemonId(cid)
	local firstStorage = getCreatureStorage(cid, FIRST_POKEMON_ON_LIST)
	return firstStorage
end

function getSecondTaskPokemonId(cid)
	local secondStorage = getCreatureStorage(cid, SECOND_POKEMON_ON_LIST)
	return secondStorage
end

function getTaskIdByName(name)
	for i = 1, #pokemonTaskTable do
		if (name == pokemonTaskTable[i].pokemon) then
			return i
		end
	end
return 0
end

function sendTaskLists(cid)
	local taskList = {}
	local str = ""
	
	for i = 1, #pokemonTaskTable do
		if (getPlayerLevel(cid) >= getTaskDifficultyLevel(pokemonTaskTable[i].difficulty)) and (getPlayerLevel(cid) < getTaskDifficultyLevel(pokemonTaskTable[i].difficulty + 1)) then
			table.insert(taskList, pokemonTaskTable[i].pokemon)
		end
	end
	
	for i = 1, #taskList do
		if i == 1 then
			str = str.."{"..taskList[i].."}"
		elseif i == #taskList then
			str = str.." and {"..taskList[i].."}"
		else
			str = str..", {"..taskList[i].."}"
		end
	end
return str
end

function canStartTask(cid, id)
	local d = pokemonTaskTable[id].difficulty
	local p = d + 1
	
	if (getPlayerLevel(cid) >= getTaskDifficultyLevel(d)) and (getPlayerLevel(cid) < getTaskDifficultyLevel(p) + 1) then
		if (getCreatureStorage(cid, getTaskStorageById(id)) < 1) then
			if (getFirstTaskPokemonId(cid) == id) or (getSecondTaskPokemonId(cid) == id) then
				return true
			else
				return false
			end
		else
			return false
		end
	end
return false
end

function startTask(cid, id)
	doCreatureSetStorage(cid, PTASK_STARTED, id)
	doCreatureSetStorage(cid, PTASK_AMOUNT_STORAGE, 0)
	doCreatureSetStorage(cid, PTASK_TIME_STORAGE, os.time() + (pokemonTaskTable[id].time * 60))
return true
end

function doFinishTask(cid, id, failed)
    doCreatureSetStorage(cid, PTASK_STARTED, -1)
    doCreatureSetStorage(cid, PTASK_AMOUNT_STORAGE, 0)
    doCreatureSetStorage(cid, PTASK_TIME_STORAGE, 0)
    doCreatureSetStorage(cid, FIRST_POKEMON_ON_LIST, 0)
