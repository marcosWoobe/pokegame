function getTaskDificultyByLevel(level)
	if (level >= 20 and level < 50) then
		return DIFFICULTY_EASY
	elseif (level >= 50 and level < 100) then
		return DIFFICULTY_MEDIUM
	elseif (level >= 100 and level < 150) then
		return DIFFICULTY_HARD
	elseif (level >= 150 and level < 999) then
		return DIFFICULTY_VERY_HARD
	end
return DIFFICULTY_NOTHING
end

function getTaskDifficultyLevel(difficulty)
	if (difficulty == DIFFICULTY_EASY) then
		return 20
	elseif (difficulty == DIFFICULTY_MEDIUM) then
		return 50
	elseif (difficulty == DIFFICULTY_HARD) then
		return 100
	elseif (difficulty == DIFFICULTY_VERY_HARD) then
		return 150
	end
return 999
end